# Steps for File Handling
# Open a file: Use the open() function.
# Modes:
# 'r': Read (default mode, file must exist).
# 'w': Write (overwrites the file or creates a new one).
# 'a': Append (adds to the file or creates a new one).
# 'r+': Read and write.
# Perform an action: Read, write, or append data.
# Close the file: Use close() to free resources.

