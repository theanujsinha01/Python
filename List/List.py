# List : collection items with different or similar 
list = [1, 'Anuj',2.5, True]
print(list)

print(list[0]) # first element of list
print(list[-1]) # last element of list

# Add element in list from last
list.append('rohit')
print(list)

# Remove element in list
list.remove('Anuj')
print(list)

# length of list
print(len(list))

# remove element from last
list.pop()
print(list)

# insert element at specific index 
list.insert(0,'ZDM')
print(list)

# clear the list
list.clear()
print(list)


