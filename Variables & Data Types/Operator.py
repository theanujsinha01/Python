# Arithmetic Operators :
a = 10
b = 5
print(a+b) # Output : 15
print(a-b) # Output : 5
print(a*b) # Output : 50
print(a/b) # Output : 2.0
print(a**b) # Output : 100000 a^b
print(a % b) # Output : 0

