# Comparison Operators (==,>,<, >=,<=,!=):
x = 10
y = 8

print(x == y) # Output : False
print(x != y) # Output : True
print(x > y) # Output : True
print(x < y) # Output : False
print(x >= y) # Output : True
print(x <= y) # Output : False

#------------------------------------------------------------------------------------------

# Assignment Operator (=): 

u = 100
print("Value of U is :",u) #Value of U is : 100

#------------------------------------------------------------------------------------------
# Logical Operator (and, or , not):

val1 = True
val2 = False

print(val1 and val2) #Output :False
print(val1 or val2) #Output :True
print(not val2) #Output :True

#------------------------------------------------------------------------------------------
# IN , NOT IN Operator :
numbers = [1, 2, 3, 4, 5]
print(3 in numbers)  # Output: True

text = "Hello"
print("H" in text)   # Output: True

print(10 not in numbers)  # Output: True
print("z" not in text)    # Output: True
